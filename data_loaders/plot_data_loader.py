@data_loader
def load_data_from_api(*args, **kwargs):
    """
    Template for loading data from API
    """
    url = 'https://jobicy.com/api/v2/remote-jobs?count=50&tag=python&geo=usa'
    response = requests.get(url)

    return {"data": response.text}


